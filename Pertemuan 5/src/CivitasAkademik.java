public class CivitasAkademik {
    private String nama;
    private String kode;

    public CivitasAkademik(String nama, String kode) {
        this.nama = nama;
        this.kode = kode;
    }

    // Getter and Setter
    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }
}



public class Pembeli {
    private String namaPembeli;
    private Integer noMeja;

    public String getNamaPembeli() {
        return namaPembeli;
    }

    public Integer getNoMeja() {
        return noMeja;
    }

    public String setNamaPembeli(String namaPembeli) {
        return this.namaPembeli = namaPembeli;
    }

    public Integer setNoMeja(Integer noMeja) {
        return this.noMeja = noMeja;
    }
}
